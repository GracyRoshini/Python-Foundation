#--list slicing operations

name=['a', 'b', 'c', 'd', 'e', 'Gracy']

print('name[2:5]: ',name[2:5]) 
print('name[3:6]: ',name[3:6]) 
print('name[-3:6]: ',name[-3:6]) 
print('name[-5:-2]: ',name[-5:-2]) 
print('name[-5:]: ',name[-5:]) 
print('name[4:5]: ',name[4:5])
print('name[4:]: ',name[4:]) 
print('name[2:]: ',name[2:]) 

